def update_weights(x, y, w, u, q):
  for i in range(len(w)):
    if w[i] == 0:
      continue

    else:
      # Update weight using gradient descent
      dw = (y - np.dot(w, x.T)) * x[i] 
      w[i] = w[i] + learning_rate * dw

      temp = w[i]

      # Implement SGD-L1 (Cumulative)
      w[i] = max(0, w[i] - (u + q)) + min(0, w[i] + (u - q))

      q = q + (w[i] - temp)
    
  return w, q

